inline std::ostream &operator<<(std::ostream &os, attribute_t at)
{
    const char *name = nullptr;
    int res = dwarf_get_AT_name(static_cast<Dwarf_Half>(at), &name);
    if (res != DW_DLV_OK) {
        os << "<bogus attrnum>";
    }
    else {
        os << name;
    }
    return os;
}
